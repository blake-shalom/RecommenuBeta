#import "UIColor+RMUColors.h"

@implementation UIColor (RMUColors)

+(UIColor*) RMULogoBlueColor
{
    return [UIColor colorWithRed:0.0f / 256.0f green:205.0f / 256.0f blue:203.0f / 256.0f alpha:1.0f];
}

+(UIColor*) RMUGrayPressedColor
{
    return [UIColor colorWithRed:215.0f / 256.0f green:215.0f / 256.0f blue:215.0f / 256.0f alpha:1.0f];
}

+(UIColor*) RMUBluePressedColor
{
    return [UIColor colorWithRed:0.0f / 256.0f green:192.0f / 256.0f blue:202.0f / 256.0f alpha:1.0f];
}

@end
